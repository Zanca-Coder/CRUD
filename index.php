            <h1>O que é CRUD:</h1>
            <p>
                <strong>CRUD</strong>é um acrônimo que representa as quatro operações básicas usadas em sistemas de
                banco de dados
                relacionais:
            </p>
            <ul>
                <li><strong>C</strong>reate (Criar): Adiciona novos registros ao banco de dados.</li>
                <li><strong>R</strong>ead (Ler): Recupera registros do banco de dados.</li>
                <li><strong>U</strong>pdate (Atualizar): Modifica registros existentes no banco de dados.</li>
                <li><strong>D</strong>elete (Deletar): Remove registros do banco de dados.</li>
            </ul>
            <p>Essas operações são fundamentais para qualquer aplicativo que lide com dados. Por exemplo, um sistema de
                gerenciamento de clientes geralmente permite que você crie, leia, atualize e delete informações de
                clientes,
                como nome, endereço, telefone, etc.</p>
            <p>O CRUD é uma das funcionalidades mais básicas e essenciais em muitos sistemas de software e é utilizado
                em
                uma variedade de aplicações, desde sistemas simples até sistemas empresariais complexos.
            </p>
